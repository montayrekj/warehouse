  },
  data() {
    return {
      table1: {
        columns: [...tableColumns],
        data: [...tableData],
        modalColumns: [...modalColumns]
      },
      modalFlag: false,
      selected: [],
      selectAll: false,
      type: '',
      tbodyClasses: '',
      quantity: [],
      search: '',
      grandTotal: 0,
      paid: 'No',
      paymentMethod: 'Cash',
      paymentType: 'Full',
      paidAmount: 0,
      balance: 0,
      chequeDueDate: null,
      remarks: null
    };
  },
  computed: {
    tableClass() {
      return this.type && `table-${this.type}`;
    },
    stocksPaid() {
        return this.$t('stocks.paid');
      }
  },
  props: {
    sample: Boolean
  },
  watch: {
    selected: function() {
      if(this.selected.length !== this.table1.data.length){
        this.selectAll = false;
      } else if(this.selected.length === this.table1.data.length){
        this.selectAll = true;
      }
    },
    search: function () {
      if(this.search != '') {
        this.table1.data = [...tableData].filter(item => 
          item.name.toUpperCase().includes(this.search.toUpperCase()) || 
          item.code.toUpperCase().includes(this.search.toUpperCase()) || 
          item.quantity.includes(this.search) ||
          item.unit.toUpperCase().includes(this.search.toUpperCase()) || 
          item.price.includes(this.search) ||
          item.supplier.toUpperCase().includes(this.search.toUpperCase()));
      }
      else
        this.table1.data = [...tableData];
    },
    paid: function() {
      if(this.paid == 'No') {
        this.paymentMethod = 'Cash',
        this.paymentType = 'Full',
        this.paidAmount = 0;
        this.balance = 0;
        this.chequeDueDate = null;
      }
    },
    paymentMethod: function() {
      this.paidAmount = 0;
      this.balance = 0;
      this.chequeDueDate = null;
    },
    paymentType: function() {
      this.paidAmount = 0;
      this.balance = 0;
      this.chequeDueDate = null;
    } ,
    paidAmount: function() {
      if(this.paidAmount != '') {
        if(this.grandTotal > this.paidAmount)
          this.balance = this.grandTotal - this.paidAmount;
        else
          this.balance = 0;
      } 
      else
        this.balance = 0;
    },
    grandTotal: function() {
      if(this.paidAmount != '' && this.paidAmount > 0) {
        if(this.grandTotal > this.paidAmount)
          this.balance = this.grandTotal - this.paidAmount;
        else
          this.balance = 0;
      }
    }
  },
  methods: {
    toggleModal: function(origin){
      this.$emit("changeSample", !this.sample)
      if(origin === "addStock") {
        if(!this.$data.modalFlag) {
          if(this.selected.length > 0){
           
            this.$refs.addStockModal.open()
            this.quantity = new Array(this.selected.length);
            for(var i = 0; i < this.quantity.length; i++){
              this.quantity[i] = 0;
            }
            this.$data.modalFlag = true;
          } else {
            this.$refs.errorStockModal.open();
          }
        } else {
          this.$refs.addStockModal.close()
          this.quantity = [];
          this.grandTotal = 0;
          this.$data.modalFlag = false;
        }
      } else if(origin === "removeStock") {
        if(!this.$data.modalFlag) {
          if(this.selected.length > 0){
            this.$refs.removeStockModal.open()
            this.quantity = new Array(this.selected.length);
            for(var i = 0; i < this.quantity.length; i++){
              this.quantity[i] = 0;
            }
            this.$data.modalFlag = true;
          } else {
            this.$refs.errorStockModal.open();
          }
        } else {
          this.$refs.removeStockModal.close()
          this.quantity = [];
          this.grandTotal = 0;
          this.$data.modalFlag = false;
        }
      }
    },
    closeErrorModal(){
      this.$emit("changeSample", !this.sample)
      this.$refs.errorStockModal.close()
    },
    isSelected(index) {
      return this.selected.indexOf(index) > -1;
    },
    getWidth(column) {
      return column === ""? "20%":""
    },
    hasValue(item, column) {
      return item[column.toLowerCase()] !== "undefined";
    },
    itemValue(item, column) {
      return item[column.toLowerCase()];
    },
    getItemById(id){
      for(var i = 0; i < this.table1.data.length; i++){
        if(this.table1.data[i].id === id){
          return this.table1.data[i];
        }
      }
    },
    quantityChangeColumn(column){
      return column === "";
    },
    selectAllStocks() {
      this.selected = [];
      if (!this.selectAll) {
          for (let i in this.table1.data) {
            this.selected.push(this.table1.data[i].id);
        }
      } 
    },
    saveStock(origin) {
      if(origin === "addStock") {
          for(var i = 0; i < this.selected.length; i++){
            for(var j = 0; j < this.table1.data.length; j++){
              if(this.table1.data[j].id === this.selected[i]){
                this.table1.data[j].quantity = Number(this.table1.data[j].quantity) + Number(this.quantity[i]);
                this.table1.data[j].quantity = this.table1.data[j].quantity.toString();
                break;
              }
            }
          }
          this.$refs.addStockModal.close()
          this.quantity = [];
          this.$data.modalFlag = false;
      } else if(origin === "removeStock") {
          for(var i = 0; i < this.selected.length; i++){
            for(var j = 0; j < this.table1.data.length; j++){
              if(this.table1.data[j].id === this.selected[i]){
                this.table1.data[j].quantity = Number(this.table1.data[j].quantity) - Number(this.quantity[i]);
                this.table1.data[j].quantity = this.table1.data[j].quantity.toString();
                break;
              }
            }
          }
          this.$refs.removeStockModal.close()
          this.quantity = [];
          this.$data.modalFlag = false;
      }
    },
    quantityChange(event, i, index, origin){
      event.preventDefault();
      var val = Number(event.target.value);
      var item = this.getItemById(i);
      if(val < 0){
        this.quantity[index] = 0;
      } else if(val > Number(item.quantity)){
        if(origin === 'removeStock'){
          this.quantity[index] = item.quantity;
        }
      }
      this.computeGrandTotal(item);
      this.$forceUpdate();
    },
    computeGrandTotal(item) {
      this.grandTotal = 0;
      for(var i=0; i<this.quantity.length; i++) {
        this.grandTotal += (item.price * this.quantity[i]);
      }
    }
  }
};
